public class PrimitiveDataTypes {
    public static void main(String[] args)
    {
      int num = 5; // has to fall within the range
      byte by = 127; 
      short sh = 5945;
      long l = 5945L;

      float f = 6f; 
      double d = 5.8;

      boolean b = true;
      
      char chr = 'k'; // can also be a number '9'
    }
}
