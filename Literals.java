public class Literals {
  public static void main(String[] args)
  {
    int num1 = 5; // we can have direct values and can work with base 10
    int num2 = 0b101; // base 2
    int num3 = 0x7E; // hexadecimal
    int num4 = 1000_000_000; // These underscores helps us to count the numbers as a programmer.
    double num5 = 54; // This will automatically convert to 54.0
    double num6 = 12e10; // 1.2E11
    char c = 'a';
    c++; // print b
    // c = c + 2;
    
    System.out.println(num1);
    System.out.println(num2);
    System.out.println(num3);
    System.out.println(num4);
    System.out.println(num5);
    System.out.println(num6);
    System.out.println(c);
    
  }
}
