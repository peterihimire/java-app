public class RelationalOperators {
  public static void main (String arg[]){
/** Relational operators includes [<, >, =, ==, != ]. Now the difference between = and == is that the first is for assingning to a variable while the second is for comparing between two values.
 * 
 * != this is for not equal to.
 * We can also combine these operators.
 *  
*/
  int numm = 7;
  int numn = 9;
  boolean data = numm > numn; // false
  boolean res = numm >= numn;
  boolean info = numm == numn;
  
  System.out.println(data);
  System.out.println(res);
  System.out.println(info);
  }
}
